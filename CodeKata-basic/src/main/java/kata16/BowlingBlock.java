package kata16;

public class BowlingBlock {
	private int pin;
	private int state;
	public BowlingBlock(int pin) {
		this.pin = pin;
	}
	public int getPin() {
		return pin;
	}
	public void setPin(int pin) {
		this.pin = pin;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	
}